int	ft_except(char *argv)
{
	int	j;
	int	a;
	int	k;

	j = 0;
	a = 0;
	k = 0;
	while (k < 16)
	{
		if (argv[j] < '1' && argv[j] > '4')
			return (a);
		j = j + 2;
		k++;
	}
	if (argv[31] != '\0')
		return (a);
	return (a + 1);
}
