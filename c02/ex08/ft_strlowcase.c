char	*ft_strlowcase(char *str)
{
	int	count;

	count = 0;
	while (*str)
	{
		if ('A' <= *str && *str <= 'Z')
			(*str) = (*str) + 32;
		str++;
		count++;
	}
	return (str - count);
}
